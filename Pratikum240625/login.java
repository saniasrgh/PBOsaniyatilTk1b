    private void appendDigit(String digit) {
        if (inputPIN.length() < 6) { // maksimal 6 digit
            inputPIN += digit;
            pw.setText("*".repeat(inputPIN.length())); // tampilkan bintang sesuai jumlah digit
        }
    }

