    private void btnbackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbackActionPerformed
            if (!inputPIN.isEmpty()) {
            inputPIN = inputPIN.substring(0, inputPIN.length() - 1);
            pw.setText("*".repeat(inputPIN.length()));
        }
    }//GEN-LAST:event_btnbackActionPerformed

    private void btnenterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnenterActionPerformed
            if (inputPIN.equals(correctPIN)) {
                JOptionPane.showMessageDialog(this, "Login Berhasil!");
                // Buka form menu utama
                new menuutama().setVisible(true);
                this.dispose(); // tutup login form
            } else {
                JOptionPane.showMessageDialog(this, "PIN Salah!", "Error", JOptionPane.ERROR_MESSAGE);
                inputPIN = "";
                pw.setText("");
            }
    }//GEN-LAST:event_btnenterActionPerformed

